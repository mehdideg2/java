    /**
     * @param stagiaire the stagiaire to set
     */
    public void setStagiaire(Stagiaire stagiaire) {
        this.stagiaire = stagiaire;
        nomTxt.setText(stagiaire.getNom());
        prenomTxt.setText(stagiaire.getPrenom());
        civiliteCombo.setSelectedItem(stagiaire.getCivilite());
        niveauCombo.setSelectedItem(stagiaire.getNiveauProgrammation());
        payeurCombo.setSelectedItem(stagiaire.getPayeur());
        SpinnerDateModel dateModel = (SpinnerDateModel) naissanceSpinner.getModel();
        if (stagiaire.getDateNaissance() != null) {
            dateModel.setValue(stagiaire.getDateNaissance());
        }
        
    }


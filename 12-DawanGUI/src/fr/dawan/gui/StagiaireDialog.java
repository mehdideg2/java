    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if (stagiaire == null) {
            stagiaire = new Stagiaire();
        }
        
        stagiaire.setNom(nomTxt.getText());
        stagiaire.setPrenom(prenomTxt.getText());
        stagiaire.setCivilite(civiliteCombo.getItemAt(civiliteCombo.getSelectedIndex()));
        stagiaire.setNiveauProgrammation((Niveau) niveauCombo.getSelectedItem());
        stagiaire.setPayeur((PriseEnCharge) payeurCombo.getSelectedItem());
        SpinnerDateModel dateModel = (SpinnerDateModel) naissanceSpinner.getModel();
        stagiaire.setDateNaissance(dateModel.getDate());
        doClose(RET_OK);
    }//GEN-LAST:event_okButtonActionPerformed

